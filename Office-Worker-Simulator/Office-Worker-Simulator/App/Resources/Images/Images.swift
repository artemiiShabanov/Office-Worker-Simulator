import UIKit

enum Images {
    enum Computer {
        static let oldKey = UIImage(named: "old_key")!
        static let oldKeyboard = UIImage(named: "old_keyboard")!
        static let oldDisplay = UIImage(named: "old_display")!
    }
    enum Office {
        enum Lofi {
            static let bg = UIImage(named: "bg_lofi")!
            static let desk = UIImage(named: "desk_lofi")!
        }
        enum Casual {
            static let bg = UIImage(named: "bg_casual")!
            static let desk = UIImage(named: "desk_casual")!
        }
    }
}
